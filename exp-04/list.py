# Iterating over a list
my_list = [1, 2, 3, 4, 5]
print("Iterating over a list:")
for item in my_list:
    print(item)

# Iterating over a dictionary
my_dict = {"name": "John", "age": 30, "city": "New York"}
print("\nIterating over a dictionary:")
for key, value in my_dict.items():
    print(f"{key}: {value}")
